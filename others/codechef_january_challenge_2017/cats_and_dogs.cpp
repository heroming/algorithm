int t;
lint c, h, l;

inline bool valid() {
    if (l % 4) return 0;
    if (l < h * 4 || (c + h) * 4 < l) return 0;
    lint x = (c + h) * 4 - l;
    return x <= h * 8;
}

int main() {
    scanf("%d", &t);
    while (t --) {
        scanf("%lld%lld%lld", &c, &h, &l);
        printf("%s\n", valid() ? "yes" : "no");
    }
    return 0;
}
